package deportista;

public class Persona{
    private String nombre;
    private int edad;

    public void setNombre(String nombre){ 
        this.nombre=nombre;
    }

    public void setEdad(String edad){ 
        this.edad=edad;
    }

    public String getNombre(){
        return this.nombre;
    }

    public String getEdad(){
        return this.edad;
    }  

    
}